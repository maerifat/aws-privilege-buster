red=`tput setaf 1`
green=`tput setaf 2`
magenta=`tput setaf 5`
reset=`tput sgr0`
bold=`tput bold`



print_usage () {

echo "${bold}${magenta}Usage:${reset}"
echo "${magenta}This tool uses credentials from ~/.aws/credentials"
echo "${magenta}All the arguments are optional" 
echo -e "\t -t (provide the number of threads)"
echo -e "\t -r (provide the aws region)"
echo -e "\t -p (provide the aws profile)"
echo -e "\t -e (Determine if the privileges can be escalated, values: true/false)"
echo -e "\t -h (know the usage of tool)"
echo -e "\t -s (provide the particular aws service to enumerate, by default all aws services will be enumerated)"
echo "EXAMPLE: ./awspbuster.sh -t 20 -p stage -r ap-south-1 -s iam -e true"
exit
return 
}

threads=1
service=""

while getopts t:r:p:he:s: flag
do
    case "${flag}" in
        t) threads=${OPTARG};;
        r) region="--region ${OPTARG}";;
        p) profile="--profile ${OPTARG}";;
	e) priv_esc=${OPTARG};;
	s) service="${OPTARG}";;
	h) print_usage;;
	*) print_usage;;
    esac
done


exectool () {
if $cmd  $region $profile > /dev/null 2> /dev/null
then
echo "${green}$cmd ${bold}[Access Granted] ${reset}"
else
echo "${red}$cmd ${bold}[Access Denied] ${reset}"
fi
}


execallservices () {
for cmd in "${cmdlist[@]}" ;do
((i=i%threads)); ((i++==0)) && wait
exectool &
done
}


getspservicelist () {
spservicecmdlist=()
printf  "'%s'\n" "${cmdlist[@]}" |grep "aws $service"|tr -d "'">tempsrv.txt
z=0
for srvcmd in $(cat tempsrv.txt|tr " " "!");do
mysrvcmd=$(echo $srvcmd|tr "!" " ")
spservicecmdlist[$z]="$mysrvcmd"
((z++))
done
}


execspservices () {
for cmd in "${spservicecmdlist[@]}" ;do
((i=i%threads)); ((i++==0)) && wait
exectool &
done
}


if [ "$service" == "" ];then
execallservices
else
getspservicelist
execspservices
fi

